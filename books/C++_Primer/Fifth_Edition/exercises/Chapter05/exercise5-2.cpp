/**
 * A compound statement, usually referred to as a block, is a (possibly empty)
 * sequence of statements and declarations surrounded by a pair of curly braces.
 * A block is a scope.  Names introduced inside a block are accessible only in
 * that block and in blocks nested inside that block.  Names are visible from
 * where they are defined until the end of the (immediately) enclosing block.
 *
 * Compound statements are used when the language requires a single statement
 * but the logic of our program needs more than one.  For example, the body of a
 * while or for loop must be a single statement, yet we often need to execute
 * more than one statement in the bod of a loop.  We do so by enclosing the
 * statements in curly braces, thus turning the sequence of statements into a
 * block.
 */
